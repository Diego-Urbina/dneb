package es.ucm.si.dneb.service.math;

public class MathServiceException extends RuntimeException{

	public MathServiceException() {
		super();
			
	}

	public MathServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MathServiceException(String arg0) {
		super(arg0);
	
	}

	public MathServiceException(Throwable arg0) {
		super(arg0);

	}
	

}
